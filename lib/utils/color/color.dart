import 'package:flutter/material.dart';

class CustomColor {
  static Color greenColor() {
    return const Color.fromARGB(255, 0, 137, 85); // Return a custom color
  }

  static Color whiteColor() {
    return const Color.fromARGB(255, 254, 254, 254); // Return a custom color
  }

  static Color blackColor() {
    return const Color.fromARGB(255, 0, 0, 0); // Return a custom color
  }

  static Color greyColor() {
    return const Color.fromARGB(255, 168, 168, 168); // Return a custom color
  }
  static Color greytextColor() {
    return Color.fromARGB(255, 87, 85, 85); // Return a custom color
  }

  static Color redColor() {
    return const Color.fromARGB(255, 246, 0, 0); // Return a custom color
  }
}
